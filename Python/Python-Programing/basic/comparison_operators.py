# Comparison Operators in Python

#==   # Equal to → True if both values are equal
#!=   # Not equal to → True if values are different
#>    # Greater than → True if left value is greater than right
#<    # Less than → True if left value is less than right
#>=   # Greater than or equal to → True if left value is greater or equal
#<=   # Less than or equal to → True if left value is less or equal


a = 10
b = 5

print(a == b)   # False
print(a != b)   # True
print(a > b)    # True
print(a < b)    # False
print(a >= b)   # True
print(a <= b)   # False

